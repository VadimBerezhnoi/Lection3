void PrintImage(int[,] image)
{
    for (int rows = 0; rows < image.GetLength(0); rows++)
    {
        for (int columns = 0; columns < image.GetLength(1); columns++)
        {
            if (image[rows, columns] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

